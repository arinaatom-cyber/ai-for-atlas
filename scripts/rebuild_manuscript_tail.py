def insert_after(anchor: Paragraph, text: str) -> Paragraph:
    new_p = OxmlElement("w:p")
    anchor._element.addnext(new_p)
    new_para = Paragraph(new_p, anchor._parent)
    if text:
        new_para.add_run(text)
    return new_para


def remove_paragraph(p: Paragraph) -> None:
    p._element.getparent().remove(p._element)


def main() -> None:
    path = DOCX.parent / DOCX.name
    doc = Document(str(path))

    anchor = next(
        p
        for p in doc.paragraphs
        if p.text.strip().startswith("Отдельно фиксировались проекты, связанные одной публикацией")
    )

    # удалить всё после §2.6 до БЛАГОДАРНОСТИ
    to_remove = []
    started = False
    for p in doc.paragraphs:
        if p is anchor:
            started = True
            continue
        if not started:
            continue
        if p.text.strip() == "БЛАГОДАРНОСТИ":
            break
        to_remove.append(p)

    for p in to_remove:
        remove_paragraph(p)

    last = anchor
    for line in TAIL:
        last = insert_after(last, line)

    doc.save(str(path))
    print(f"Rebuilt tail: {path} ({len(TAIL)} paragraphs inserted)")


if __name__ == "__main__":
    main()
