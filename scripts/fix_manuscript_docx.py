def insert_before(anchor: Paragraph, text: str) -> Paragraph:
    new_p = OxmlElement("w:p")
    anchor._element.addprevious(new_p)
    new_para = Paragraph(new_p, anchor._parent)
    if text:
        new_para.add_run(text)
    return new_para


def insert_after(anchor: Paragraph, text: str) -> Paragraph:
    new_p = OxmlElement("w:p")
    anchor._element.addnext(new_p)
    new_para = Paragraph(new_p, anchor._parent)
    if text:
        new_para.add_run(text)
    return new_para


def remove_paragraph(p: Paragraph) -> None:
    el = p._element
    el.getparent().remove(el)


def has_methods_27(doc: Document) -> bool:
    found_toc = False
    for p in doc.paragraphs:
        if p.text.strip().startswith("2.7. Трёхуровневая архитектура") and "Presence layer" in p.text:
            return True
        if p.text.strip().startswith("2.7. Трёхуровневая архитектура"):
            if found_toc:
                return "Presence layer" in p.text
            found_toc = True
        if p.text.strip().startswith("2.8. Автоматизированный мониторинг"):
            return False
    return False


